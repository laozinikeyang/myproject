package entity;

public class SysUserinfo {
    private String sysUserid;

    private String sysUsername;

    private String sysSex;

    private Integer sysAge;

    private String sysLanguage;

    private String sysProfession;

    private String sysName;

    public String getSysUserid() {
        return sysUserid;
    }

    public void setSysUserid(String sysUserid) {
        this.sysUserid = sysUserid;
    }

    public String getSysUsername() {
        return sysUsername;
    }

    public void setSysUsername(String sysUsername) {
        this.sysUsername = sysUsername;
    }

    public String getSysSex() {
        return sysSex;
    }

    public void setSysSex(String sysSex) {
        this.sysSex = sysSex;
    }

    public Integer getSysAge() {
        return sysAge;
    }

    public void setSysAge(Integer sysAge) {
        this.sysAge = sysAge;
    }

    public String getSysLanguage() {
        return sysLanguage;
    }

    public void setSysLanguage(String sysLanguage) {
        this.sysLanguage = sysLanguage;
    }

    public String getSysProfession() {
        return sysProfession;
    }

    public void setSysProfession(String sysProfession) {
        this.sysProfession = sysProfession;
    }

    public String getSysName() {
        return sysName;
    }

    public void setSysName(String sysName) {
        this.sysName = sysName;
    }
}